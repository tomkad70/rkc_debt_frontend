<script setup>
import {ref, computed, watch} from 'vue'
import api from '../api'

const emit = defineEmits(['close', 'created'])
const name = ref('')
const email = ref('')
const password = ref('')
const password_confirmation = ref('')
const role = ref('lawyer')
const error = ref(null)
const isSubmitting = ref(false)
const showPassword = ref(false)
const showConfirmPassword = ref(false)

const passwordMismatch = computed(() => {
  return password.value && password_confirmation.value &&
      password.value !== password_confirmation.value
})

// Clear error when form values change
watch([name, email, password, password_confirmation, role], () => {
  error.value = null
})

const submit = async () => {
  // Client-side validation
  if (passwordMismatch.value) {
    error.value = '–ü–∞—Ä–æ–ª–∏ –Ω–µ —Å–æ–≤–ø–∞–¥–∞—é—Ç'
    return
  }

  if (password.value.length < 8) {
    error.value = '–ü–∞—Ä–æ–ª—å –¥–æ–ª–∂–µ–Ω —Å–æ–¥–µ—Ä–∂–∞—Ç—å –º–∏–Ω–∏–º—É–º 8 —Å–∏–º–≤–æ–ª–æ–≤'
    return
  }

  error.value = null
  isSubmitting.value = true

  try {
    await api.post('/users', {
      name: name.value,
      email: email.value,
      password: password.value,
      password_confirmation: password_confirmation.value,
      role: role.value
    })

    emit('created')
  } catch (err) {
    console.error('Error creating user:', err)

    if (err.response?.data?.errors) {
      // Extract first validation error
      const firstError = Object.values(err.response.data.errors)[0]
      error.value = Array.isArray(firstError) ? firstError[0] : firstError
    } else {
      error.value = err.response?.data?.message || '–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è'
    }
  } finally {
    isSubmitting.value = false
  }
}
</script>

